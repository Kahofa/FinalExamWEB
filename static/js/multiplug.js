function updateText(language) {
    const elementsWithKey = document.querySelectorAll('[key]');
    elementsWithKey.forEach((element) => {
        const key = element.getAttribute('key');
        element.innerHTML = translations[language][key];
    });

    localStorage.setItem('selectedLanguage', language);
}

document.addEventListener('click', (event) => {
    const targetButton = event.target.closest('.multilang-btn, .phone-multilang-btn');

    if (targetButton) {
        const language = targetButton.getAttribute('key');

        if (translations[language]) {
            updateText(language);

            document.querySelectorAll('.multilang-btn, .phone-multilang-btn').forEach((button) => {
                button.classList.remove('active');
            });

            targetButton.classList.add('active');
        } else {
            console.error('Translations not available for language:', language);
        }
    }
});

const savedLanguage = localStorage.getItem('selectedLanguage');

